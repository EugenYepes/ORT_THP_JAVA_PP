package com.activity03.mains;

class Ej_22 {

	public static void main(String[] args) {
        for (int i = 1; i <= 5; i++) {
            System.out.print(i + "\t");
        }
        System.out.println();
        for (int i = 5; i >= 1; i--) {
            System.out.print(i + "\t");
        }
    }
}